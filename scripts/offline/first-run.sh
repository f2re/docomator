#!/usr/bin/env bash
set -Eeuo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CONFIG_FILE="/etc/docomator/docomator.env"
EXPLICIT_URL=""
RUN_CHECK=0

usage() {
  cat <<'USAGE'
Использование: first-run.sh [параметры]

Показывает пошаговую памятку первого запуска установленного Docomator.
Помощник работает автономно и не изменяет данные.

Параметры:
  --config ФАЙЛ   файл настроек (по умолчанию: /etc/docomator/docomator.env)
  --url АДРЕС     явный адрес веб-интерфейса
  --check         проверить готовность локальных компонентов перед показом памятки
  -h, --help      показать эту справку
USAGE
}

while (($# > 0)); do
  case "$1" in
    --config) CONFIG_FILE="$2"; shift 2 ;;
    --url) EXPLICIT_URL="$2"; shift 2 ;;
    --check) RUN_CHECK=1; shift ;;
    -h|--help) usage; exit 0 ;;
    *) printf 'Неизвестный параметр: %s\n' "$1" >&2; exit 2 ;;
  esac
done

read_value() {
  local key="$1"
  [[ -f "$CONFIG_FILE" ]] || return 0
  grep -E "^[[:space:]]*${key}=" "$CONFIG_FILE" | tail -n 1 | cut -d= -f2- || true
}

if [[ -n "$EXPLICIT_URL" ]]; then
  UI_URL="${EXPLICIT_URL%/}"
else
  HOST="$(read_value DOCOMATOR_HOST)"
  PORT="$(read_value DOCOMATOR_PORT)"
  [[ -n "$HOST" ]] || HOST="127.0.0.1"
  [[ -n "$PORT" ]] || PORT="8080"
  case "$HOST" in
    0.0.0.0|::) HOST="127.0.0.1" ;;
  esac
  UI_URL="http://${HOST}:${PORT}"
fi

if ((RUN_CHECK == 1)); then
  NODE=""
  HEALTHCHECK=""
  for candidate in \
    /opt/docomator/current/runtime/node/bin/node \
    "$SCRIPT_DIR/payload/runtime/node/bin/node"; do
    if [[ -x "$candidate" ]]; then NODE="$candidate"; break; fi
  done
  for candidate in \
    /opt/docomator/current/app/scripts/offline/healthcheck.mjs \
    "$SCRIPT_DIR/healthcheck.mjs"; do
    if [[ -f "$candidate" ]]; then HEALTHCHECK="$candidate"; break; fi
  done

  if [[ -n "$NODE" && -n "$HEALTHCHECK" ]]; then
    if "$NODE" "$HEALTHCHECK" "$UI_URL/readyz" 5000 >/dev/null 2>&1; then
      printf '✅ Локальная служба готова: %s/readyz\n' "$UI_URL"
    else
      printf '⚠️  Локальная служба пока не готова. Проверьте «systemctl status docomator-api» и повторите запуск с --check.\n' >&2
    fi
  else
    printf 'ℹ️  Автоматическая проверка службы недоступна: не найден встроенный Node.js или файл healthcheck.mjs.\n'
  fi

  PREVIEW_ENABLED="$(read_value DOCOMATOR_PREVIEW_ENABLED)"
  LIBREOFFICE_BIN="$(read_value DOCOMATOR_LIBREOFFICE_BIN)"
  [[ -n "$PREVIEW_ENABLED" ]] || PREVIEW_ENABLED="true"
  [[ -n "$LIBREOFFICE_BIN" ]] || LIBREOFFICE_BIN="/usr/bin/libreoffice"
  if [[ "$PREVIEW_ENABLED" == "true" ]]; then
    if [[ -x "$LIBREOFFICE_BIN" ]]; then
      printf '✅ LibreOffice доступен для создания PDF: %s\n\n' "$LIBREOFFICE_BIN"
    else
      printf '⚠️  Предварительный просмотр включён, но LibreOffice не найден: %s\n   Установите пакеты из автономного комплекта либо исправьте DOCOMATOR_LIBREOFFICE_BIN.\n\n' "$LIBREOFFICE_BIN" >&2
    fi
  else
    printf 'ℹ️  Предварительный просмотр отключён настройкой DOCOMATOR_PREVIEW_ENABLED.\n\n'
  fi
fi

cat <<EOF
🧩 Docomator установлен. Первый запуск

Откройте интерфейс:
  $UI_URL/

Рекомендуемый порядок:
  1. 🧑‍🤝‍🧑 Создайте или выберите пространство.
     Пространство изолирует людей, группы, исходники шаблонов и будущие документы.

  2. 🧱 Проверьте тип «Человек» в разделе «Схема данных».
     При необходимости добавьте свойства: ФИО, должность, рост, вес и другие параметры.

  3. 👥 Добавьте участников в выбранное пространство.
     Данные другого пространства не появятся в этом списке.

  4. ☑️ Отметьте нужных людей.
     Выбор можно использовать один раз или сохранить как именованную группу.

  5. 📋 Выберите форму результата:
     • «Один общий документ» — таблица или список всех выбранных участников;
     • «По документу на каждого» — отдельный документ для каждого участника.

  6. 📸 Зафиксируйте снимок состава.
     Состав останется неизменным, даже если группу отредактируют позже.

  7. 🛡️ Откройте раздел «Шаблоны» и проверьте DOCX или XLSX.
     Система покажет макросы, внешние связи, опасные пути и превышение ограничений.

  8. 🔒 После успешной проверки отдельно подтвердите сохранение исходника.
     Выберите пространство и нажмите «Сохранить исходник». Система повторно проверит файл,
     вычислит SHA-256 и сохранит неизменяемую копию без создания дубликата.

  9. 🧭 Нажмите «Построить структуру».
     Система покажет абзацы DOCX или ячейки XLSX, их значения, формулы и устойчивые координаты.
     Для крупных файлов показывается ограниченная выборка с полными итоговыми количествами.

  10. 📌 Выберите область заполнения и сохраните поле.
      Укажите понятное название, технический ключ, тип значения и обязательность.
      Сервер заново читает сохранённый исходник и проверяет выбранную координату.

  11. 🧪 Выполните пробное заполнение.
      Выберите черновик, поле и значение. Система создаст техническую привязку,
      запишет значение в отдельную копию, считает его обратно и сохранит обе версии только при совпадении.

  12. 🧩 Если полей несколько, откройте «Проверить все поля».
      Заполните весь набор. Система создаст одну техническую и одну проверенную копию,
      а затем повторно считает каждое значение уже из окончательного файла.

  13. 👁️ Создайте предварительный просмотр базовой проверенной версии.
      Фоновый обработчик запускает LibreOffice с отдельным временным профилем.
      Состояние сохраняется: страницу можно закрыть и открыть позже.

  14. ✅ Просмотрите PDF и отдельно подтвердите активацию.
      Активная версия появится в каталоге только после подтверждения. Исторические версии не изменяются.

Что уже работает:
  ✅ пространства, изоляция, участники и группы;
  ✅ выбор всех, группы или отмеченных людей;
  ✅ точный расчёт: один документ или несколько документов;
  ✅ безопасная проверка DOCX/XLSX с русскими замечаниями;
  ✅ подтверждаемое неизменяемое хранение исходника по SHA-256;
  ✅ абзацы, текстовые фрагменты, листы, ячейки и формулы;
  ✅ устойчивые идентификаторы элементов и контрольная сумма структуры;
  ✅ черновики шаблонов и проверяемые скалярные поля;
  ✅ техническая привязка целого абзаца DOCX или одной ячейки XLSX;
  ✅ пробное значение, обратное чтение и неизменяемые проверенные копии;
  ✅ полный набор из нескольких полей, история и две многополевые копии;
  ✅ сохраняемая очередь создания PDF, понятная ошибка и явный повтор;
  ✅ просмотр PDF и отдельное подтверждение активации;
  ✅ неизменяемые активные версии и каталог текущего пространства;
  ✅ журнал действий и неизменяемые снимки состава.

Что появится следующим этапом:
  ⏳ PDF и активация многополевой версии;
  ⏳ подстановка списка участников в повторяющуюся таблицу или список;
  ⏳ создание одного общего либо нескольких индивидуальных документов;
  ⏳ окончательное скачивание, автоматизация и доставка.

Помощь находится внутри каждого раздела по кнопке «❓ Помощь».
EOF
